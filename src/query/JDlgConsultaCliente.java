    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:
        //se cpf e nome estiverem em branco:
        if(jTxtNome.getText().equals("") && jTxtCpf.getText().equals("")) { //se os campos forem vazios
            List lista = cliente_DAO.listALL(); 
            clienteControle.setList(lista);
        }else { //caso N√ÉO estejam vazios
                if(!jTxtNome.getText().equals("") && !jTxtCpf.getText().equals("")) { 
                List lista = cliente_DAO.listNomeCpf(jTxtNome.getText(), jTxtCpf.getText()); 
                clienteControle.setList(lista);
                }else {
                    if (!jTxtNome.getText().equals("")) {
                    List lista = cliente_DAO.listNome(jTxtNome.getText());
                    clienteControle.setList(lista);
                    }else {
                        if (!jTxtCpf.getText().equals("")){
                        List lista = cliente_DAO.listCpf(jTxtCpf.getText());
                        clienteControle.setList(lista);
                        }
                    }
                }  
            }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

